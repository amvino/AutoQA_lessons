#Написать программу, которая определяет являться ли год високосным.
#1. Год является високосным если он делится на 4 без остатка.
#2. Исключение: Если год делится на 100 без остатка, он не является високосным, только если год не делится на 400 без остатка. В этом случае, он все равно является високосным.

year = int(input('Введите год: '))

if year % 4 == 0 and year % 100 != 0 or year % 400 == 0:
    print(f'{year} - високосный')
else:
    print(f'{year} - невисокосный')

