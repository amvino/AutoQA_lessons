#Написать программу, которая будет в зависимости от возраста и суммы товара выдавать правильные скидки (то есть выводить сумму с учетом скидки):
#Если покупателю < 18 скидка 5% независимо от суммы покупки;
#Если покупателю от 18 до 60 и сумма покупки больше 5000 то скидка 10%;
#Если покупателю от 18 до 60 и сумма покупки меньше или равна 5000 то скидка будет 7%;
#Если покупателю больше 60 лет, то продукты бесплатно (шучу, скидка 15%)

age = int(input('Возраст:'))
amount =  int(input('Cумма покупки:'))

if age < 18:
    discount = 0.05
elif 18 <= age <= 60:
    if amount > 5000:
        discount = 0.10
    else:
        discount = 0.07
else:
    discount = 0.15

discount_amount = amount * discount
final_amount = amount - discount_amount

print(f'Покупателю в возрасте {age} полагается скидка {discount * 100}%. Итоговая стоимость с учетом скидки составит {final_amount}.')